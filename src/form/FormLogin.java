    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        Tools.readConfigFile();
        String username = txtUsername.getText().trim();
        String pass = new String(txtPassword.getPassword());
        String sql = "SELECT * FROM tblAccount WHERE Username = ? AND Password = ?";
        
        Connection cn = Tools.getConn();
        if(cn == null) {
            JOptionPane.showMessageDialog(this, "Không thể đăng nhập"
                    + "to DB, check "
                    + "jdbc driver, and then check Config info");
            new FormConfig(this, true).setVisible(true);
            return;
        }
//        btnLogin.setBorder(new LineBorder(Color.RED, 3));
//        try {
//            Thread.sleep(4000);
//        } catch (InterruptedException ex) {
//            Logger.getLogger(FormLogin.class.getName()).log(Level.SEVERE, null, ex);
//        }
        try (
            PreparedStatement pst = cn.prepareStatement(sql);
        ) {
            pst.setString(1, username);
            pst.setString(2, pass);
            ResultSet rs = pst.executeQuery();
            if(rs.next()) {
                ModalAccount currentAccount = new ModalAccount(
                        rs.getString(2), rs.getInt(4));
                ShareData.getInstance().setAccount(currentAccount);
                ApartmentManagementForm amf = new ApartmentManagementForm();
                amf.setLocationRelativeTo(this);
                amf.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Login fail");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormLogin.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                cn.close();
            } catch (SQLException ex) {
                Logger.getLogger(FormLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

