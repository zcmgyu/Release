package form;

/**
 *
 * @author PhiLong
 */
public class ModalGender {
    private int id;
    private String gender;

    public ModalGender() {
    }

    public ModalGender(int id, String gender) {
        this.id = id;
        this.gender = gender;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }
    
    @Override
    public String toString() {
        return gender;
    }
}
